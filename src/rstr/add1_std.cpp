uchar stdUpperAscii( uchar c )
{
  if ( c >= (uchar)'a' && c <= (uchar)'z')
    c = c - (uchar)'a' + (uchar)'A';

  if ( c >= (uchar)'\xa0' && c <= (uchar)'\xAF')
    c = c - (uchar)'\xa0' + (uchar)'\x80';
  if ( c >= (uchar)'\xe0' && c <= (uchar)'\xEF')
    c = c - (uchar)'\xe0' + (uchar)'\x90';
return c;
}

 /* Function returns LOWER CASE variant of the letter.             */
uchar stdLowerAscii(uchar c)
{
  if ( c >= (uchar)'A' && c <= (uchar)'Z')
    c = c - (uchar)'A'+ (uchar)'a' ;
  if ( c >= (uchar)'\x80' && c <= (uchar)'\x8f')
    c = c - (uchar)'\x80'+ (uchar)'\xA0' ;
  if ( c >= (uchar)'\x90' && c <= (uchar)'\x9f')
    c = c - (uchar)'\x90'+ (uchar)'\xE0' ;
return c;
}
