#include "recognitionstate.h"

namespace cf {

RecognitionState::RecognitionState() :
        state_(NONE)
{}

void RecognitionState::setCallback(Callback cb) {
    cb_ = cb;
}

void RecognitionState::set(state_t st) {
    state_ = st;

    if(cb_)
        cb_(state_);
}

}
