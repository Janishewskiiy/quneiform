#include "scanarea.h"

ScanArea::ScanArea(QWidget * parent) :
    QLabel(parent)
{
    const int WIDTH = 100;
    const int HEIGHT = WIDTH / (595.276 / 841.89);

    setFixedSize(WIDTH, HEIGHT);
    QPixmap p(WIDTH, HEIGHT);
    p.fill(Qt::gray);
    setPixmap(p);
}
